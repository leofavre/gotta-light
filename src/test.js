const initialState = {
	canvas: {
		width: 800,
		height: 600
	},
	lightSource: [0, 0],
	phrase: gotLight,
	ray: {
		gap: 8,
		width: 50,
		height: 4
	}
};

const updateGap = (state, action) => ({
	...state,
	gap: action.gap
});

const updateSize = (state, action) => ({
	...state,
	width: action.width,
	height: action.height
});

const lightSource = (state = initialState.lightSource, action) => {
	switch (action.type) {
		case "UPDATE_LIGHT_SOURCE":
			return action.coords;

		default:
			return state;
	}
};

const phrase = (state = initialState.phrase, action) => {
	switch (action.type) {
		case "UPDATE_PHRASE":
			return action.source;

		default:
			return state;
	}
};

const ray = (state = initialState.ray, action) => {
	switch (action.type) {
		case "UPDATE_RAY_GAP":
			return updateGap(state, action);

		case "RESIZE_RAY":
			return updateSize(state, action);

		default:
			return state;
	}
};

const canvas = (state = initialState.canvas, action) => {
	switch (action.type) {
		case "RESIZE_STAGE":
			return updateSize(state, action);

		default:
			return state;
	}
};

const app = (state = initialState, action) => {
	return {
		lightSource: lightSource(state.lightSource, action),
		phrase: phrase(state.phrase, action),
		ray: ray(state.ray, action),
		canvas: canvas(state.canvas, action)
	};
};

const store = Redux.createStore(app);

const calculatePhraseWidthInPixels = (phrase, ray) => 1 + ray.gap * phrase[0].length;

const calculatePhraseHeightInPixels = (phrase, ray) => 1 + ray.gap * phrase.length;

const calculatePhraseStartCoords = (canvas, phrase, ray) => {
	let phraseWidth = calculatePhraseWidthInPixels(phrase, ray),
		phraseHeight = calculatePhraseHeightInPixels(phrase, ray);

	return [(canvas.width - phraseWidth) / 2, (canvas.height - phraseHeight) / 2];
};

const render = () => {
	const { lightSource, phrase, ray, canvas } = store.getState(),
		[startX, startY] = calculatePhraseStartCoords(canvas, phrase, ray),
		gap = ray.gap;

	phrase.forEach((line, lineIndex) => {
		line.forEach((dot, dotIndex) => {
			console.log([
				Math.round(dotIndex * gap + startX),
				Math.round(lineIndex * gap + startY)
			]);
		});
	});
};

render();
store.subscribe(render);
