const initialState = {
	canvas: {
		width: 800,
		height: 600
	},
	lightSource: [0, 0],
	phrase: gotLight,
	ray: {
		gap: 8,
		width: 50,
		height: 4
	}
};

const updateGap = (state, action) => ({
	...state,
	gap: action.gap
});

const updateSize = (state, action) => ({
	...state,
	width: action.width,
	height: action.height
});

const lightSource = (state = initialState.lightSource, action) => {
	switch (action.type) {
		case "UPDATE_LIGHT_SOURCE":
			return action.coords;

		default:
			return state;
	}
};

const phrase = (state = initialState.phrase, action) => {
	switch (action.type) {
		case "UPDATE_PHRASE":
			return action.source;

		default:
			return state;
	}
};

const ray = (state = initialState.ray, action) => {
	switch (action.type) {
		case "UPDATE_RAY_GAP":
			return updateGap(state, action);

		case "RESIZE_RAY":
			return updateSize(state, action);

		default:
			return state;
	}
};

const canvas = (state = initialState.canvas, action) => {
	switch (action.type) {
		case "RESIZE_STAGE":
			return updateSize(state, action);

		default:
			return state;
	}
};

const app = Redux.combineReducers({
	lightSource,
	phrase,
	ray,
	canvas
});

const store = Redux.createStore(app);

const calculatePhraseWidthInPixels = (phrase, gap) => 1 + gap * phrase[0].length;

const calculatePhraseHeightInPixels = (phrase, gap) => 1 + gap * phrase.length;

const calculateTopLeftCoord = (canvas, phrase, gap) => {
	let phraseWidth = calculatePhraseWidthInPixels(phrase, gap),
		phraseHeight = calculatePhraseHeightInPixels(phrase, gap);

	return [
		Math.round((canvas.width - phraseWidth) / 2),
		Math.round((canvas.height - phraseHeight) / 2)
	];
};

const calculateCoord = (xStart, yStart, xIndex, yIndex, gap) =>
	[Math.round(xIndex * gap + xStart), Math.round(yIndex * gap + yStart)];

const calculateVisibleCoords = (canvas, phrase, gap) => {
	let [xStart, yStart] = calculateTopLeftCoord(canvas, phrase, gap);

	return phrase
		.map((line, lineIndex) => {
			return line
				.map((dot, dotIndex) => !!dot ? calculateCoord(xStart, yStart, dotIndex, lineIndex, gap) : null)
				.filter(coord => coord != null);
		})
		.reduce((result, nextArr) => result.concat(nextArr), []);
};

const render = () => {
	let { lightSource, phrase, ray, canvas } = store.getState(),
		visibleCoords = calculateVisibleCoords(canvas, phrase, ray.gap);

	console.log(visibleCoords);
};

render();
store.subscribe(render);